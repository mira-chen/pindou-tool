/**
 * 获取指定品牌、指定套装的颜色列表
 * @param {string} brandName - 品牌名称 ("MARD", "COCO", "ManMan", "PanPan", "MiXiaoWo")
 * @param {string} setName - 套装名称 ("SET_24", "SET_48", ... "SET_295")
 * @returns {Array} 颜色对象数组
 */
function getSetColors(brandName, setName) {
    // 1. 获取该套装对应的 MARD 色号列表 (作为基准)
    const baseCodes = SETS_CONFIG[setName];
    if (!baseCodes) {
        console.error(`套装 ${setName} 不存在`);
        return [];
    }

    // 2. 找到这些色号对应的 Global ID
    // 我们先从 ALL_BRANDS["MARD"] 里找到这些颜色，拿到它们的 global_id
    const targetIds = ALL_BRANDS["MARD"]
        .filter(item => baseCodes.includes(item.name))
        .map(item => item.global_id);

    // 3. 根据 Global ID 去目标品牌里找对应的颜色
    if (!ALL_BRANDS[brandName]) {
        console.error(`品牌 ${brandName} 不存在`);
        return [];
    }

    const result = ALL_BRANDS[brandName].filter(item => targetIds.includes(item.global_id));

    // 按 ID 排序，保证展示顺序一致
    return result.sort((a, b) => a.global_id - b.global_id);
}

