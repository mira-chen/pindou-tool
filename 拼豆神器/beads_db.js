// 2. 生成品牌视图的函数 (处理 "-" 情况)
function generateBrandView(masterData) {
    const brandView = {
        "MARD": [],
        "COCO": [],
        "ManMan": [],
        "PanPan": [],
        "MiXiaoWo": []
    };

    masterData.forEach(color => {
        const addColor = (brandKey, code) => {
            // 如果色号为空、undefined 或 "-"，则不添加
            if (!code || code === "" || code === "-") return;

            brandView[brandKey].push({
                name: code,
                r: color.rgb[0],
                g: color.rgb[1],
                b: color.rgb[2],
                hex: color.hex,
                global_id: color.id
            });
        };

        addColor("MARD", color.brands.mard);
        addColor("COCO", color.brands.coco);
        addColor("ManMan", color.brands.manman);
        addColor("PanPan", color.brands.panpan);
        addColor("MiXiaoWo", color.brands.mixiaowo);
    });

    return brandView;
}

